                            About That Time I Got Reincarnated as a Slime
                            <br/><br/>
                            The TV anime That Time I Got Reincarnated as a Slime is an entertaining isekai story that revolves around the main character who dies and gets reincarnated in another world as a slime. He makes full use of the various skills that he learns as he explores this new world, and through his courage and wisdom he gains friends along the way. The anime, which is referred to as “Tensura,” first aired on TOKYO MX and other broadcasting stations from October 2018 to March 2019.<br/><br/>Then in 2021 from January to September, the first cour of its second season, the first Tensura spin-off anime based on the comic The Slime Diaries: That Time I Got Reincarnated as a Slime, and the second cour of season 2 all aired consecutively for those nine months.<br/><br/>Currently it's being broadcast nationwide on each of the Nippon Television Network System stations (every Saturday at 0:55 a.m. on Nippon TV)! The original comic, That Time I Got Reincarnated as a Slime (serialized in Kodansha's Monthly Shōnen Sirius / Manga: Taiki Kawakami, Original Author: Fuse, Character Drafts: Mitz Vah), has reached 900 million page views on the novel posting site “Let's Become a Novelist.” Taking all of the series' various related books into account—Mr. Fuse's original light novel, the comic adaptation of his popular light novel of the same name, and the spin-off works, more than 30 million copies are in circulation.

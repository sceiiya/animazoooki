                            Mushoku Tensei: Jobless Reincarnation (無職転生〜異世界行ったら本気だす〜 Mushoku Tensei Isekai Ittara Honki Dasu) is a high fantasy anime series based on the original Mushoku Tensei series of light novels written by Rifujin na Magonote. The series is being produced by Egg Firm and directed by Manabu Okamoto at Studio Bind. The anime officially began broadcasting on Japanese television from January 10, 2021.
                            <br/><br/>
                            Production and Development<br/>
                            On March 15, 2019, the official website of MF Books announced that an anime adaptation project of Mushoku Tensei would be produced. It was confirmed on October 16, 2019, with the release of the first promotional video that the adaptation will be a television series slated to premiere in October 2020. It was also revealed Manabu Okamoto will be directing the series at Studio Bind (along with other staff-member reveals) with Egg Firm producing the series. [2] On June 19, 2020, however, it was announced the release has been delayed to 2021.[3] It was announced on December 6, 2021 that the anime will officially start broadcasting on television from January 10, 2021 onwards.[4] An early broadcast of the first two episodes was released on December 27, 2020 on the NicoNico streaming service. The first cour began with on January 10, 2021 broadcasted on Tokyo MX, Sun TV, KBS Kyoto and BS11 and ended on March 21.
                            <br/><br/>
                            After the first cour, it was announced on May 19, 2021 that the premiere of second cour which was slated for July has been postponed to October and the anime will re-broadcast the first eleven episodes from July 11 onwards. Also confirming the release of Blu-ray Chapter 3 and 4 were delayed. It was announced on August 16, 2021 that the second cour will officially start broadcasting on television from October 3, 2021 onwards.
                            Mushoku Tensei will appear at Anime Japan 2023, making it an excellent opportunity for all fans to catch up on the latest announcements of the anime.
